// Math.abs(): Trả về giá trị tuyệt đối của một số
console.log(Math.abs(-5.5)); // |-5.5| = 5.5
console.log(Math.abs(10));   // |10| = 10

// Math.ceil(): Làm tròn lên số nguyên gần nhất
console.log(Math.ceil(4.4)); // 4.4 làm tròn lên thành 5
console.log(Math.ceil(5.8)); // 5.8 làm tròn lên thành 6

// Math.floor(): Làm tròn xuống số nguyên gần nhất
console.log(Math.floor(5.7)); // 5.7 làm tròn xuống thành 5
console.log(Math.floor(6.1)); // 6.1 làm tròn xuống thành 6

// Math.round(): Làm tròn đến số nguyên gần nhất
console.log(Math.round(10.1)); // 10.1 làm tròn xuống gần 10 hơn
console.log(Math.round(20.6)); // 20.6 làm tròn lên gần 21 hơn